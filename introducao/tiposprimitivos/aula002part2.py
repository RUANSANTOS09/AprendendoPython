n = input('Digite algo:')
print(n.isnumeric()) # Analisa e retorna True ou False, se valor é numerico ou não
print(n.isalpha()) # Analisa e retorna True ou False, se o valor é letra ou não
print(n.isalnum()) # Analisa e retorna True ou False, se o valor é alfabetico ou numerico
print(n.isupper()) # Analisa e retorna True ou False, se o valor é uppercase